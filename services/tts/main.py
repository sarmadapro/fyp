# Global Kokoro state
_pipeline = None
_kokoro_voice_ready = False  # True only after the voice file is confirmed cached

_last_inference_at: float = 0.0   # updated by real requests AND keepalive
_KEEPALIVE_IDLE_SEC: float = 8 * 60   # run keepalive when idle > 8 min
_KEEPALIVE_CHECK_SEC: float = 60      # poll every 60 s


def get_pipeline():
    """Load the Kokoro TTS pipeline (model weights only, no voice files)."""
    global _pipeline
    if _pipeline is None:
        from kokoro import KPipeline
        logger.info(f"Loading Kokoro TTS pipeline (lang={TTS_LANG_CODE})...")
        _pipeline = KPipeline(lang_code=TTS_LANG_CODE)
        logger.info("Kokoro TTS pipeline loaded successfully.")
    return _pipeline


# ── Request Models ───────────────────────────────────────────────────

class SynthesizeRequest(BaseModel):
    text: str
    voice: str = TTS_VOICE
    language: str = "en"   # ISO 639-1 language code from STT detection


# ── Synthesis Helpers ────────────────────────────────────────────────

def _kokoro_synthesize(text: str, voice: str) -> bytes:
    """Synthesize using local Kokoro model (English only). Raises on failure."""
    pipeline = get_pipeline()
    audio_chunks = []

    for _, _, audio in pipeline(text, voice=voice):
        if audio is not None:
            audio_chunks.append(audio)

    if not audio_chunks:
        raise ValueError("No audio generated by Kokoro.")

    import numpy as np
    full_audio = np.concatenate(audio_chunks)

    wav_buffer = io.BytesIO()
    sf.write(wav_buffer, full_audio, 24000, format="WAV", subtype="PCM_16")
    return wav_buffer.getvalue()


def _try_warm_kokoro_voice() -> bool:
    """
    Attempt to synthesize a short test string to force the voice file to be
    downloaded and cached. Returns True on success, False on failure.
    Called at startup so the first real request is fast and offline-safe.
    """
    global _kokoro_voice_ready
    try:
        _kokoro_synthesize("Hello.", TTS_VOICE)
        _kokoro_voice_ready = True
        # Lock HuggingFace Hub to offline mode so it never tries to re-fetch
        os.environ["HF_HUB_OFFLINE"] = "1"
        logger.info(f"Kokoro voice '{TTS_VOICE}' cached and ready (offline mode enabled).")
        return True
    except Exception as e:
        _kokoro_voice_ready = False
        logger.warning(
            f"Kokoro voice pre-warm failed (will use Edge-TTS for English as fallback): {e}"
        )
        return False


def _pyttsx3_synthesize(text: str) -> bytes:
    """
    Offline fallback: Windows SAPI (pyttsx3). No internet required.
    Works on any Windows machine with built-in voices (e.g. Microsoft David/Zira).
    Saves to a temp WAV file then returns the bytes.
    """
    import pyttsx3
    import tempfile

    engine = pyttsx3.init()
    engine.setProperty("rate", 175)   # slightly slower than default (200)
    engine.setProperty("volume", 1.0)

    tmp_path = None
    try:
        with tempfile.NamedTemporaryFile(suffix=".wav", delete=False) as f:
            tmp_path = f.name
        engine.save_to_file(text, tmp_path)
        engine.runAndWait()
        with open(tmp_path, "rb") as f:
            return f.read()
    finally:
        engine.stop()
        if tmp_path:
            try:
                os.unlink(tmp_path)
            except OSError:
                pass


async def _edge_tts_synthesize(text: str, language: str) -> bytes:
    """Synthesize using Edge-TTS (Microsoft) for multilingual support."""
    import edge_tts

    voice = EDGE_TTS_VOICE_MAP.get(language)
    if not voice:
        # Try base language code (e.g. "zh-CN" → "zh")
        base = language.split("-")[0]
        voice = EDGE_TTS_VOICE_MAP.get(base)

    if not voice:
        logger.warning(
            f"No Edge-TTS voice found for language '{language}'. "
            "Falling back to en-US-JennyNeural."
        )
        voice = "en-US-JennyNeural"

    logger.info(f"[Edge-TTS] Synthesizing '{text[:60]}...' with voice={voice}")

    communicate = edge_tts.Communicate(text, voice)
    mp3_chunks: list[bytes] = []

    async for chunk in communicate.stream():
        if chunk["type"] == "audio":
            mp3_chunks.append(chunk["data"])

    if not mp3_chunks:
        raise ValueError("Edge-TTS returned no audio chunks.")

    # Convert MP3 → WAV using soundfile + numpy via io
    import numpy as np
    mp3_bytes = b"".join(mp3_chunks)

    # soundfile can read MP3 if libsndfile supports it; fall back to pydub if not
    try:
        audio_data, sample_rate = sf.read(io.BytesIO(mp3_bytes), dtype="int16")
    except Exception:
        # pydub fallback (requires ffmpeg)
        try:
            from pydub import AudioSegment
            segment = AudioSegment.from_file(io.BytesIO(mp3_bytes), format="mp3")
            wav_buf = io.BytesIO()
            segment.export(wav_buf, format="wav")
            return wav_buf.getvalue()
        except Exception as e2:
            raise RuntimeError(f"Could not decode Edge-TTS audio: {e2}") from e2

    wav_buffer = io.BytesIO()
    sf.write(wav_buffer, audio_data, sample_rate, format="WAV", subtype="PCM_16")
    return wav_buffer.getvalue()


# ── Startup & Health ─────────────────────────────────────────────────

async def _keepalive_loop():
    """
    Background loop that re-synthesizes a short string whenever Kokoro has been
    idle for _KEEPALIVE_IDLE_SEC.  Keeps the model loaded and ONNX/GPU context
    alive so the next real request starts instantly.
    """
    global _last_inference_at
    while True:
        await asyncio.sleep(_KEEPALIVE_CHECK_SEC)
        if not _kokoro_voice_ready:
            continue
        idle = time.time() - _last_inference_at
        if idle >= _KEEPALIVE_IDLE_SEC:
            logger.info(
                f"[TTS] Keep-alive triggered (idle {idle / 60:.1f} min) — running warmup synthesis."
            )
            try:
                await asyncio.to_thread(_kokoro_synthesize, "Ready.", TTS_VOICE)
                _last_inference_at = time.time()
                logger.info("[TTS] Keep-alive synthesis done.")
            except Exception as e:
                logger.warning(f"[TTS] Keep-alive failed: {e}")


@app.on_event("startup")
async def startup_warmup():
    async def _warm():
        global _last_inference_at
        try:
            # Step 1: load the model weights
            await asyncio.to_thread(get_pipeline)
            # Step 2: force voice file download/cache with a test synthesis
            await asyncio.to_thread(_try_warm_kokoro_voice)
            # _try_warm_kokoro_voice already runs a full synthesis pass,
            # which warms up all kernels.  Stamp the inference clock so the
            # keepalive doesn't immediately re-fire after startup.
            _last_inference_at = time.time()
        except Exception as e:
            logger.error(f"TTS warmup failed: {e}")

    asyncio.create_task(_warm())
    asyncio.create_task(_keepalive_loop())


