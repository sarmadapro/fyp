# ── Endpoints ────────────────────────────────────────────────────────

@app.post("/synthesize")
async def synthesize(request: SynthesizeRequest):
    """
    Synthesize text to speech.

    - English (language="en"): uses local Kokoro model (fast, offline)
    - Other languages:         uses Edge-TTS (Microsoft, requires internet)

    Returns: WAV audio bytes.
    """
    if not request.text.strip():
        raise HTTPException(status_code=400, detail="Text cannot be empty.")

    if len(request.text) > 5000:
        raise HTTPException(status_code=400, detail="Text too long. Maximum 5000 characters.")

    global _last_inference_at
    _last_inference_at = time.time()
    start_time = _last_inference_at
    lang = (request.language or "en").lower().split("-")[0]  # normalize "en-US" → "en"

    wav_bytes: bytes | None = None
    engine = "unknown"

    # ── Level 1: Kokoro (English only, offline, best quality) ──────────
    if lang == "en" and _kokoro_voice_ready:
        try:
            wav_bytes = await asyncio.to_thread(_kokoro_synthesize, request.text, request.voice)
            engine = "kokoro"
        except Exception as e:
            logger.warning(f"Kokoro failed: {e}")

    # ── Level 2: Edge-TTS (multilingual, internet required) ────────────
    if wav_bytes is None:
        try:
            wav_bytes = await _edge_tts_synthesize(request.text, lang)
            engine = "edge-tts"
        except Exception as e:
            logger.warning(f"Edge-TTS failed (lang={lang}): {e}")

    # ── Level 3: pyttsx3 (Windows SAPI, fully offline, English only) ──
    if wav_bytes is None:
        try:
            wav_bytes = await asyncio.to_thread(_pyttsx3_synthesize, request.text)
            engine = "pyttsx3"
            logger.info("Using pyttsx3 (Windows SAPI) as offline fallback.")
        except Exception as e:
            logger.error(f"pyttsx3 also failed: {e}")

    if wav_bytes is None:
        logger.error(f"All TTS engines failed for lang={lang}")
        raise HTTPException(status_code=503, detail="All TTS engines unavailable. Check network and service logs.")

    duration = time.time() - start_time
    logger.info(
        f"[{engine}] Synthesized {len(request.text)} chars in {duration:.2f}s "
        f"| language={lang}"
    )

    return Response(
        content=wav_bytes,
        media_type="audio/wav",
        headers={
            "X-Engine": engine,
            "X-Language": lang,
            "X-Processing-Time": str(round(duration, 2)),
        },
    )


@app.post("/synthesize/stream")
async def synthesize_stream(request: SynthesizeRequest):
    """
    Streaming synthesis for English (Kokoro only).
    Non-English falls back to non-streaming /synthesize endpoint.
    """
    if not request.text.strip():
        raise HTTPException(status_code=400, detail="Text cannot be empty.")

    lang = (request.language or "en").lower().split("-")[0]

    if lang != "en":
        # Delegate to non-streaming endpoint for multilingual
        return await synthesize(request)

    try:
        from fastapi.responses import StreamingResponse

        pipeline = get_pipeline()
        sample_rate = 24000

        async def audio_generator():
            for _, _, audio in pipeline(request.text, voice=request.voice):
                if audio is not None:
                    chunk_buffer = io.BytesIO()
                    sf.write(chunk_buffer, audio, sample_rate, format="WAV", subtype="PCM_16")
                    yield chunk_buffer.getvalue()

        return StreamingResponse(audio_generator(), media_type="audio/wav")

    except Exception as e:
        logger.error(f"Streaming synthesis error: {e}")
        raise HTTPException(status_code=500, detail=f"Streaming synthesis failed: {str(e)}")


